"KLD Explorer syftar till att vara ett verktyg för grafisk och statistisk analys av data ur
databasen KOLADA. Applikationen består därför av följande funktioner:

1. En kolumndatabas för att lagra variabler utifrån dimensionerna Nyckeltal, Kommun och År

2. Funktioner för att importera och lagra data från KOLADA - eller andra datakällor som till-
   handahåller data på kolumnanpassatformat

3. Grafiska analysverktyg för att utforska t.ex. tidsserier, jämföra olika kommuner eller år,
   eller undersöka storleksförhållanden mellan olika variabler

4. Numerisk analys av data för att ge viss statistisk precision åt analyser, och 

5. Länkar till originaldata i källsystemen.


I 'technology preview'-versionen är punkterna 1 och 2 fullständigt implementerade, samt delar
av punkt 3 ovan.

KLD Explorer är utvecklat i Python och R. Användargränssnittet är byggt i R-scriptspråket Shiny,
vilket möjliggör framtida integration av t.ex. JavaScript för ett större mått av interaktivitet
i den grafiska analysen.

Om du vill vara med och bidra till utvecklinen av KLD Explorer, hittar du länkar till all
information du behöver under fliken 'Mer...' ovan.

Nyheter om uppdateringar av KLD Explorer kommer att postas här på applikationssidan, och på
vår utveckingsblogg visuelldata.wordpress.com.


