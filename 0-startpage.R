"Välkommen till KLD Explorer - en applikation för att utforska data ur Kommun- och Landstings-
databasen KOLADA.

KLD Explorer är ett verktyg för att på grafisk och numerisk väg utforska KOLADA-data. Datat i
applikationen är nedladdat direkt från KOLADA:s hemsida, och bör därför svara exakt mot det
data som KOLADA erbjuder på sin egen hemsida, www.kolada.se. (Vi reserverar oss mot eventuella
felaktigheter som beror på felinmatning av data i vår databas. Eventuella resultat bör alltid
dubbelkollas mot källan, www.kolada.se!)


Detta är en s.k. 'Technology preview'. Det innebär att applikationen är under utveckling, men
att (delar av) den teknologi som används för att lagra och visualisera redan nu är redo för
testning och användning av intresserade 


Projektet drivs på ideell basis av Love Hansson och Joakim Lundborg. Målsättningen är att bygga
en fungerande, interaktiv applikation som underlättar utforskande av KOLADA-data. KOLADA är ett
väldigt spännande projekt 

För mer information om KLD Explorer och upphovsmännen, se vår blogg:
http://visuelldata.wordpress.com



