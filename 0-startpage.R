"KLD Explorer är ett verktyg för att på grafisk och numerisk väg utforska KOLADA-data. 

Klicka på Tidsserier eller Tvåvägsplot ovan för att börja!

Datan i applikationen är hämtad direkt från KOLADA:s hemsida www.kolada.se, men vi reserverar 
oss mot eventuella felaktigheter som beror på felinmatning av data i vår databas. Kontrollera 
alltid dina resultat mot källan!

************************************************************************************************
OBS! Detta är en teknikdemonstration. Det innebär att applikationen är under utveckling, men
att (delar av) den teknologi som används för att lagra och visualisera redan nu är redo för
testning och användning av intresserade 
************************************************************************************************

Projektet drivs på ideell basis av Love Hansson och Joakim Lundborg. Målsättningen är att bygga
en fungerande, interaktiv applikation som gör det enkelt att utforska KOLADA-data. KOLADA är ett
projekt som finansieras av Sveriges Kommuner och Landsting och svenska staten för att tillgänglig-
göra nyckeltalsdata om den gemensamt finansierade verksamheten i Sveriges samtliga kommuner och
landsting.

För mer information om KLD Explorer och upphovsmännen, se vår blogg:
http://visuelldata.wordpress.com

För mer information om KOLADA, se deras hemsida. Där kan du även få tillgång till hela
KOLADA:s databas, med över 2200 kvalitets- och bakgrundsnyckeltal ur kommunalt och landstings-
finansierad verksamhet:
http://www.kolada.se

KLD Explorer är öppen källkod. Denna är fritt tillgänglig via projektets kodförråd på GitHub:
https://github.com/prenumerant/KLD_Explorer

Om du är intresserad av att bidra till utvecklingen av KLD Explorer, hör gärna av dig till
oss och utveckla gärna funktionaliteten i KLD Explorer. I kodförrådet på GitHub finns all
information du behöver för att kunna hjälpa till, inklusive en roadmap för kommande releaser.



